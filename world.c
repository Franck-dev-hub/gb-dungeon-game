INT8 cam_x = 0;
INT8 cam_y = 0;

void world_init(void)
{
	int	      i, j;
	unsigned char tile_white[16] = {
		// clang-format off
		0b00000000, 0b00000000,
		0b00000000, 0b00000000,
		0b00000000, 0b00000000,
		0b00000000, 0b00000000,
		0b00000000, 0b00000000,
		0b00000000, 0b00000000,
		0b00000000, 0b00000000,
		0b00000000, 0b00000000,
		// clang-format on
	};
	unsigned char tile_black[16] = {
		// clang-format off
		0b11111111, 0b11111111,
		0b11111111, 0b11111111,
		0b11111111, 0b11111111,
		0b11111111, 0b11111111,
		0b11111111, 0b11111111,
		0b11111111, 0b11111111,
		0b11111111, 0b11111111,
		0b11111111, 0b11111111,
		// clang-format on
	};

	set_bkg_data(0, 1, tile_white);
	set_bkg_data(1, 1, tile_black);

	for (i = 0; i < MAP_HEIGHT; i++)
	{
		for (j = 0; j < MAP_WIDTH; j++)
		{
			if (map[i][j])
				map[i][j] = 1;
			else
				map[i][j] = 0;
		}
	}

	set_bkg_tiles(0, 0, MAP_WIDTH, MAP_HEIGHT, (unsigned char*)map);
	SHOW_BKG;
}

void world_scroll(void)
{
	INT8 cam_speed_x = 0;
	INT8 cam_speed_y = 0;

	if (joypad() & J_LEFT	&& cam_x > -36)	cam_speed_x = -1;
	if (joypad() & J_RIGHT	&& cam_x < 124)	cam_speed_x = 1;
	if (joypad() & J_UP	&& cam_y > -56)	cam_speed_y = -1;
	if (joypad() & J_DOWN	&& cam_y < 120)	cam_speed_y = 1;

	cam_x += cam_speed_x;
	cam_y += cam_speed_y;

	scroll_bkg(cam_speed_x, cam_speed_y);
}
